 * @mixin \Eloquent
 */
class City extends Model
{

    protected  $connection = 'formDB';
    protected $table = "city";

    public function state()
    {
        return $this->belongsTo(State::class);
    }

    public function booms()
    {
        return $this->hasMany(Boom::class);
    }
}
